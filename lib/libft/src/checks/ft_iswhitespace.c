#include "libft.h"

int	ft_iswhitespace(char c)
{
	if (c == ' '
		|| c == '\t'
		|| c == '\r'
		|| c == '\n'
		|| c == '\v'
		|| c == '\f')
		return (1);
	return (0);
}
